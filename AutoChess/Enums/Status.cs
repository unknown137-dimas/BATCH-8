/// <summary>
/// Represents the different status of the game.
/// </summary>
public enum Status
{
    /// <summary>
    /// The game is not yet initialized.
    /// </summary>
    NotInitialized,
    
    /// <summary>
    /// The game is initialized.
    /// </summary>
    Initialized,
    
    /// <summary>
    /// The game is running.
    /// </summary>
    OnGoing,
    
    /// <summary>
    /// The game is ended.
    /// </summary>
    End
}